const calculator = {
  add: function (a: number, b: number): number {
    return a + b;
  },

  subtract: function (a: number, b: number): number {
    return a - b;
  },

  multiply: function (a: number, b: number): number {
    return a * b;
  },

  divide: function (a: number, b: number): number {
    return a / b;
  },
};

export default calculator;
